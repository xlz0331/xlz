package com.hwagain.org.job.dto;

import java.io.Serializable;

import com.hwagain.framework.core.dto.BaseDto;

/**    
 * 体系、部门、工厂等级下定编定员详情
 * @author: huangyj   
 * @date: 2018年6月14日 
 */
public class OrgJobDesignDetailDto extends BaseDto implements Serializable {
	private static final long serialVersionUID = 1L;
	
	private String deptId;
	
	private String deptParentId;
	
	private String nodeName;
	
	private String longNodeName;
	
	private String jobCode;
	
	private String jobName;
	
	private String jobCompile;
	
	private String jobPeople;

	public String getDeptId() {
		return deptId;
	}

	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}

	public String getDeptParentId() {
		return deptParentId;
	}

	public void setDeptParentId(String deptParentId) {
		this.deptParentId = deptParentId;
	}

	public String getNodeName() {
		return nodeName;
	}

	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}

	public String getLongNodeName() {
		return longNodeName;
	}

	public void setLongNodeName(String longNodeName) {
		this.longNodeName = longNodeName;
	}

	public String getJobCode() {
		return jobCode;
	}

	public void setJobCode(String jobCode) {
		this.jobCode = jobCode;
	}

	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	public String getJobCompile() {
		return jobCompile;
	}

	public void setJobCompile(String jobCompile) {
		this.jobCompile = jobCompile;
	}

	public String getJobPeople() {
		return jobPeople;
	}

	public void setJobPeople(String jobPeople) {
		this.jobPeople = jobPeople;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
